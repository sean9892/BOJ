#include<bits/stdc++.h>
using namespace std;
using lint=long long;
const lint mod=1e9+7;

lint get_nth(vector<lint> rec, vector<lint> dp, lint n){
	lint m = rec.size();
	vector<lint> s(m), t(m);
	s[0] = 1;
	if(m != 1) t[1] = 1;
	else t[0] = rec[0];
	auto mul = [&rec](vector<lint> v, vector<lint> w){
		lint m = v.size();
		vector<lint> t(2 * m);
		for(lint j=0; j<m; j++){
			for(lint k=0; k<m; k++){
				t[j+k] += 1ll * v[j] * w[k] % mod;
				if(t[j+k] >= mod) t[j+k] -= mod;
			}
		}
		for(lint j=2*m-1; j>=m; j--){
			for(lint k=1; k<=m; k++){
				t[j-k] += 1ll * t[j] * rec[k-1] % mod;
				if(t[j-k] >= mod) t[j-k] -= mod;
			}
		}
		t.resize(m);
		return t;
	};
	while(n){
		if(n & 1) s = mul(s, t);
		t = mul(t, t);
		n >>= 1;
	}
	lint ret = 0;
	for(lint i=0; i<m; i++) ret += 1ll * s[i] * dp[i] % mod;
	return ret % mod;
}

